# Python handles large numbers natively without overflow, so you can work with very large integers
large_number = 10**100  # A very large number (1 followed by 100 zeros)
print(f"A large number: {large_number}")

# Computing large exponentiation
result = 2**1000  # 2 raised to the power of 1000
print(f"2 raised to the power of 1000 is: {result}")
